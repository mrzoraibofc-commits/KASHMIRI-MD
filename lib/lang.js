/**
 * Get translation string for a given key.
 * @param {string} lang - 'en' or 'fr'
 * @param {string} key  - translation key
 * @param {...any} args - optional arguments for function-based translations
 * @returns {string}
 */
function getLang(lang, key, ...args) {
    const langData = translations[lang] || translations['en'];
    const val = langData[key] ?? translations['en'][key] ?? `[${key}]`;
    if (typeof val === 'function') return val(...args);
    return val;
}

module.exports = { getLang, translations };
